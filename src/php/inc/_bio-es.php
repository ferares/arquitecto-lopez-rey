<p>
  En 1963 inicia la docencia como ayudante de taller de 1º año en la
  Facultad de Arquitectura en el taller Isern que dirige el Arq. Herrán, y
  continúa desarrollando su actividad en el estudio.
  <br>
  Becado por el Gobierno Italiano a fines de 1963, viaja a Europa y
  permanece estudiando en el Politécnico de Milán. Allí estudia
  prefabricación y cálculo matemático de probabilidades de encuentro de
  piezas con el profesor Sacchi. En febrero de 1964 conoce en su estudio
  de Milán al Arq. Ernesto Rogers con quien mantiene una inolvidable
  charla, y luego visita la Torre Velasca una de las obras más singulares
  de toda su extensa trayectoria. Se radica y trabaja hasta fines de 1964
  en la ciudad de Milán. Comparte esta radicación con un valioso grupo
  cultural italiano. Entre sus integrantes, se destacan el pintor Paolo
  Schiavocampo y el escultor Pino Spagnulo.
</p>
<p>
  Es invitado para proyectar el reciclaje de un palacio del siglo XVIII en
  Piazzale Baracca de Milano. Proyecta también un conjunto de apartamentos
  y comercios en Cinisello Balsamo zona suburbana de Milán. Finalmente
  para la misma empresa desarrolla un proyecto de viviendas prefabricadas
  con estructura metálica que se producen en una planta de armado de
  automóviles para luego montarlas en diversos lugares de la península
  italiana. En esta etapa se desarrollan las
  <a href='#thumbImg_Fantasias_Italianas' class='jspHijack'>
    fantasías italianas
  </a>
  .
</p>
<p>
  A fines de 1964 el <strong>Arq. Ramón González Almeida,</strong> ya
  radicado en Venezuela, lo invita a enseñar arquitectura en la Facultad
  de Arquitectura de la ciudad de Mérida en Venezuela. Resuelve volver a
  Montevideo aunque antes visita diversas ciudades de España, Francia,
  Bélgica, Holanda y Alemania. En este último país visita la Escuela de
  Ulm y conoce a su Director <strong>Tomás Maldonado</strong>. No pudo
  olvidar el clima monástico de alta intelectualidad que allí se percibía.
  Luego, al pasar por Suiza intenta visitar a <strong>Max Bill</strong>
  fundador de dicha escuela, pero en ese momento no se encontraba en
  Zúrich. Culmina su viaje regresando a Italia y finalmente a Grecia en
  donde permanece más de un mes visitando Atenas, Miconos, Santorini,
  Delos, Paros y Creta.
</p>
<p>
  De regreso a Montevideo en 1965 se reintegra al estudio y continúa en la
  docencia como ayudante de clase en el mismo taller del Arq. Herrán en la
  Facultad de Arquitectura. Allí se reencuentra con los mismos alumnos que
  había dejado a fines de 1963, ahora cursando 3º Año. Estudiantes
  destacados: Enrique Cohe, Roberto Alberti, Oscar Corlazzoli, Juan Elbio
  Nicola, Armando González y Julio Bazzano, entre otros.
</p>
<p>
  Desde el viaje hacia Italia, en octubre de 1963, hasta su regreso a
  principios de 1965, se incorpora Alfredo Rudich al estudio de forma
  provisoria, invitado por el arquitecto López Rey. En este período se
  realiza la residencia Poyo Roc.
</p>
<p>
  A partir de 1965 el estudio va desarrollando una nueva expresión
  plástica en la que abandona, en ocasiones, el lenguaje estructural
  inicial dando lugar a la arquitectura de fuertes volúmenes blancos. Este
  lenguaje contó con la influencia de la arquitectura mediterránea del sur
  de Italia, como Ostuni, las islas griegas, Santorini, Miconos y varias
  ciudades de Andalucía. El estudio va así en busca de este nuevo lenguaje
  que se expresa en tantas residencias y algunos edificios realizados en
  Punta del Este y Montevideo, a saber:
