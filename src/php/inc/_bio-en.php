</p>
<p>
  He joins the School of Architecture of Montevideo in 1952. During his freshman
  year, he takes part in
  <strong>
    architect Mario Paysée Reyes's
  </strong>
  workshop. Then he joins
  <strong>
    architect Carlos Gómez Gavazzo’s
  </strong>
  workshop -a great master of architecture- where he stays until the end of his
  academic years.
  <strong>
    Architect Luis Basil and Assistant Professor architect Ramón González
    Almeida
  </strong>
  -among others- were also part of this workshop. The latter gave him guidelines
  in the fields of creativity, coherence and the importance of structural order.
  He works in his firm until 1955, when
  <strong>
    architect González Almeida
  </strong> travels to the USA and then settles in Venezuela. This is when
  <strong>
    López Rey
  </strong>
  who is still a student, takes the reigns of
  <strong>
    Ramón González Almeida’s
  </strong>
  firm, though he remains professionally represented by the firm of architects
  <strong>
    Alberto Muñoz del Campo
  </strong>,
  <strong>
    Enrique Muñoz
  </strong>
  and
  <strong>
    Guillermo Gómez Platero
  </strong>.
  It is in this firm where he starts
  working on several architectural projects, such as a call for tender organized
  by Banco Hipotecario. This project was deeply appreciated by
  <strong>
    architect Rino Levi, juror
  </strong>.
  <strong>
    Architect Rodolfo López Rey
  </strong>
  meets
  <strong>
    architect Paul Rudolph
  </strong>
  during the latter’s visit to
  <strong>
    architect Gómez Gavazzo’s
  </strong>
  workshop in the School of Architecture. Here, he conducts an excellent class.
  <strong>
    Rodolfo López Rey</strong>
    admires
  <strong>
    Paul Rudolph
  </strong>
  for his homes in Sarasota (Florida, USA), published in the Arts and
  Architecture journal. Other important architects who were his role models
  during his academic years were:
  <strong>
    Le Corbusier (Charles Édouard Jeanneret), Walter Gropius, Ludwig Mies
    van der Rohe, Philip Johnson, Ernesto Rogers, Charles Eames and Harry
    Seidler.
  </strong>
</p>
