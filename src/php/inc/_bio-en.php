<p>
  In 1963 he takes up teaching as a 1st year assistant of Isern workshop, held by
  <strong>
    architect Herrán
  </strong>,
  at the School of Architecture, and continues working for the firm.
</p>
<p>
  By the end of 1963, the Italian Government awards him with a scholarship and
  he travels to Europe. He studies at Milan Polytechnic University with
  Professor Sacchi. In February 1964 he meets Architect Ernesto Rogers in his
  Milan firm  with whom he has an unforgettable conversation. López Rey then
  visits the Velasca Tower, one of the most singular works in all of Rogers’
  extensive career. He resides and works in Milan until the end of 1964. There
  he meets a valuable Italian cultural group, integrated by the outstanding
  <strong>
    painter Paolo Schiavocampo
  </strong>
  and the
  <strong>
    sculptor Pino Spagnulo
  </strong>,
  among others.
</p>
<p>
  He is invited to design the reform works of an 18th century palace at Piazzale
  Baracca de Milano. He also designs a series of apartments and shops at
  Cinisello Balsamo, a suburban area of Milan. Finally, he designs a project for
  the same company aimed at the construction of pre-manufactured houses with a
  metal structure. The assembly parts are built at a car assembly factory.
  During this stage he creates the
  <a href='#thumbImg_Fantasias_Italianas' class='jspHijack'>
    Italian fantasies
  </a>.
</p>
<p>
  At the end of 1964, he is invited by
  <strong>
    architect Ramón González Almeida
  </strong>,
  who was already residing in Venezuela, to teach architecture at the School of
  Architecture of Mérida, Venezuela. He decides to return to Montevideo, but
  before doing so he travels to several cities in Spain, France, Belgium, the
  Netherlands and Germany. In this last country he visits the Ulm School of
  Design and meets its director,
  <strong>
    Tomás Maldonado
  </strong>.
  He could not forget the intellectual and monastic climate that reigned there.
  Later, when in Switzerland, he tried to meet with
  <strong>
    Max Bill
  </strong>,
  founder of said school, who at the time was not in Zurich. He ends his journey
  revisiting Italy and finally Greece where he stays for over a month visiting
  Athens, Mykonos, Santorini, Delos, Paros and Crete.
</p>
<p>
  Back in Montevideo, in 1965 he returns to the firm and continues teaching as
  an assistant professor at
  <strong>
    architect Herrán's
  </strong>
  workshop, in the School of Architecture. There he reencounters his students
  from 1963, who are now in 3rd year. Prominent students:
  <strong>
    Enrique Cohe, Roberto Alberti, Oscar Corlazzoli, Juan Elbio Nicola,
    Armando González
  </strong>
  and
  <strong>
    Julio Bazzano
  </strong>,
  among others.
</p>
<p>
  Since his trip to Italy in October 1963 and until his return in early 1965,
  architect Alfredo Rudich temporarily joins the firm, invited by architect
  López Rey. During this period, the construction of Poyo Roc Residential House
  is finished.
</p>
<p>
  Since 1965 the firm starts developing a new plastic arts expression that
  leaves aside, at times, its initial structural language making way for strong,
  white architectural volumes. This language was influenced by the Mediterranean
  culture of Southern Italy cities such as Ostuni, the Greek islands, Santorini,
  Mykonos and various cities in Andalucía. Thus, the firm is in search of a new
  language, which finds its reflection in many residential houses as well as in
  some buildings built in Punta del Este and Montevideo, namely:
