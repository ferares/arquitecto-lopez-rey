  <strong>
    Roberto Alberti
  </strong>
  –in 1976- join as members of the firm. At first,
  <strong>
    architect Enrique Cohe
  </strong>
  collaborates by managing the construction works in Punta del Este. Then, he
  enters the project area, while
  <strong>
    architect Roberto Alberti
  </strong>
  works as an important collaborator in the area of budgeting, management and
  administration of the construction works.
</p>
