<p>
  The buildings El Fortín, Paz Marina, Arrecifes and Yacht, as well as many
  residential houses especially reflect this architectural language. They
  continue using exposed reinforced concrete in a significant series of works
  developed thanks to
  <strong>
    accountant Luis E. Lecueder’s
  </strong>
  enterprising talent. This series starts with
  <em>
    Berna
  </em>
  Building in 1969, which precedes the creation of
  <strong>
    SAFEMA
  </strong>
  (Sociedad Anónima Fomento Edilicio de Maldonado - Maldonado Building
  Development Corporation). The main reason for the success of SAFEMA was its
  incorporation and integration; a group of friends from different fields. As
  the Chair, Luis Lecueder, who did an impeccable job running the company, was
  supported by the management of the López Rey - Gómez Platero Architects firm.
</p>
<p>
  <strong>
    Luis E. Lecueder
  </strong>,
  accountant, was the true creator of the
  <strong>
    SAFEMA
  </strong>
  (Sociedad Anónima de Fomendo Edilicio de Maldonado- Maldonado Building
  Development Corporation) structure. Together with his talent, commitment,
  optimism, integrity and outstanding professionalism he accomplished important
  investments during complicated financial times in Uruguay. As a consequence,
  thanks to his important and memorable administration, so many high-quality
  buildings were erected in Punta del Este and Montevideo. We were fortunate to
  be able to walk by his side during those long and unforgettable years. Sadly,
  he left us too soon, although his son,
  <strong>
    Carlos Alberto Lecueder
  </strong>,
  was able to carry on so many new and high-quality buildings with the same
  conditions he still brings to the table today.
</p>
<p>
  Then, the three most prestigious, longstanding and excellent real estate
  companies in the area,
  <strong>
    Gattas, Sader y Paullier
  </strong>,
  start participating. A triangle of excellent professional management was
  consolidated. First, there was a tender for the reinforced concrete works,
  starting with the electrical and sanitary installations. Then there was a
  tender for masonry work with the same company or a more convenient one. After
  that, all subcontracts which had to be involved in the process at a strict
  time were written. The firm approved payments, if required, according to the
  quality of work and timeliness thereof.
  <strong>
    The Lecueder firm
  </strong>
  received this information and immediately, after checking, payments were made.
  The construction company did not manage the funds. They went directly to the
  subcontractor or supplier. This allowed those involved in the work to promptly
  receive the money for the finished work. This ended with the tradition that
  involved the main company receiving the total amount of certificates. As a
  result,
  <strong>
    SAFEMA
  </strong>
  earned a solid reputation in Montevideo and Punta del Este, which enabled it
  to do a great number of works. The apartments were always ready within the
  agreed time.
</p>
<p>
